import { maxIdx } from './index';

describe('index.ts test', () => {
  describe('maxIdx', () => {
    test('maxIdx([1, 2, 3, 4, 5]) should be 4', () => {
      expect(maxIdx([1, 2, 3, 4, 5])).toBe(4);
    });
    test('maxIdx', () => {
      expect(maxIdx([4, 2, 5, 7, 1, 3, 5, 22, 24, 1, 1, 3])).toBe(8);
    });
    test('maxIdx', () => {
      expect(
        maxIdx([
          0.15737207809343592, 0.059040789614992546, 0.10060978779298291,
          0.11019864446231445, 0.09292268133954049, 0.0934011129863232,
          0.0995729357265924, 0.09449458901862764, 0.11411609391861141,
          0.07827128704657915,
        ])
      ).toBe(0);
    });
  });
});
